# ====================== –û–±—Ä–∞–±–æ—Ç–∫–∞ –æ—Ç–≤–µ—Ç–æ–≤ ======================
async def process_answer(message: Message):
    user_id = message.from_user.id
    bot = message.bot
    
    if user_id not in active_polls:
        return
    
    poll = active_polls[user_id]
    poll_type = poll["poll_type"]
    questions = poll["questions"]
    current_question = poll["current_question"]
    answers = poll["answers"]
    
    print(f"üìù –û–±—Ä–∞–±–æ—Ç–∫–∞ –æ—Ç–≤–µ—Ç–∞ –¥–ª—è –æ–ø—Ä–æ—Å–∞ {poll_type}, –≤–æ–ø—Ä–æ—Å {current_question + 1}/{len(questions)}")
    
    # –î–æ–±–∞–≤–ª—è–µ–º –æ—Ç–≤–µ—Ç
    answers.append(message.text)
    print(f"‚úÖ –û—Ç–≤–µ—Ç –¥–æ–±–∞–≤–ª–µ–Ω: {message.text}")
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –µ—Å—Ç—å –ª–∏ –µ—â–µ –≤–æ–ø—Ä–æ—Å—ã
    if current_question + 1 < len(questions):
        # –û–±–Ω–æ–≤–ª—è–µ–º —Ç–µ–∫—É—â–∏–π –≤–æ–ø—Ä–æ—Å
        active_polls[user_id]["current_question"] = current_question + 1
        
        # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º —Å–ª–µ–¥—É—é—â–∏–π –≤–æ–ø—Ä–æ—Å
        next_question = questions[current_question + 1]
        await message.answer(next_question)
        print(f"üì® –û—Ç–ø—Ä–∞–≤–ª–µ–Ω —Å–ª–µ–¥—É—é—â–∏–π –≤–æ–ø—Ä–æ—Å: {current_question + 2}/{len(questions)}")
    else:
        # –û–ø—Ä–æ—Å –∑–∞–≤–µ—Ä—à–µ–Ω
        print(f"‚úÖ –û–ø—Ä–æ—Å {poll_type} –∑–∞–≤–µ—Ä—à–µ–Ω –¥–ª—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è {user_id}")
        
        # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –∑–∞–≤–µ—Ä—à–∞—é—â–∏–π —Ç–µ–∫—Å—Ç
        if poll.get("outro_text"):
            await message.answer(poll["outro_text"])
        
        # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º —Ç–µ–∫—Å—Ç-–Ω–∞–ø–æ–º–∏–Ω–∞–Ω–∏–µ
        if poll.get("reminder_text"):
            await message.answer(poll["reminder_text"])
        user_name = await get_user_name(user_number=None, user_id=None, telegram_id=user_id)
        # –§–æ—Ä–º–∏—Ä—É–µ–º —Å–≤–æ–¥–∫—É –¥–ª—è –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä–∞
        summary_parts = [f"üìä –†–µ–∑—É–ª—å—Ç–∞—Ç—ã –æ–ø—Ä–æ—Å–∞ {poll_type} –æ—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è {user_name}:"]
        
        for i, (question, answer) in enumerate(zip(questions, answers), 1):
            summary_parts.append(f"\n{i}. {question}\n   –û—Ç–≤–µ—Ç: {answer}")
        
        summary_text = "\n".join(summary_parts)
        
        # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä—É
        try:
            await bot.send_message(ADMIN_ID, summary_text)
            print(f"üì® –û—Ç–≤–µ—Ç—ã –æ—Ç–ø—Ä–∞–≤–ª–µ–Ω—ã –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä—É {ADMIN_ID}")
        except Exception as e:
            print(f"[send_to_admin ERROR] {e}")
        
        # –û—á–∏—â–∞–µ–º –¥–∞–Ω–Ω—ã–µ
        if user_id in active_polls:
            del active_polls[user_id]
            print(f"‚úÖ –î–∞–Ω–Ω—ã–µ –æ–ø—Ä–æ—Å–∞ –æ—á–∏—â–µ–Ω—ã –¥–ª—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è {user_id}")

# ====================== –†–µ–≥–∏—Å—Ç—Ä–∞—Ü–∏—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è ======================
async def reg_users(
    department_id: int, number: str, name: str, telegram_id: int, bot: Bot
) -> bool:
    """
    –†–µ–≥–∏—Å—Ç—Ä–∞—Ü–∏—è –Ω–æ–≤–æ–≥–æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –∏ –∑–∞–ø—É—Å–∫ –æ–ø—Ä–æ—Å–æ–≤
    """
    async with async_session() as session:
        try:
            print(f"üìù –†–µ–≥–∏—Å—Ç—Ä–∞—Ü–∏—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è: {name}, telegram_id: {telegram_id}")
            
            # –ü—Ä–æ–≤–µ—Ä–∫–∞ —Å—É—â–µ—Å—Ç–≤—É—é—â–µ–≥–æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
            existing_user = await session.scalar(
                select(db.User).where(
                    (db.User.telegram_id == telegram_id) | (db.User.number == number)
                )
            )
            if existing_user:
                print(f"‚ùå –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å —É–∂–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç: {telegram_id}")
                return False

            org_id = await session.scalar(
                select(db.Department.organization_id).where(db.Department.id == department_id)
            )
            if not org_id:
                print(f"‚ùå –û—Ç–¥–µ–ª –Ω–µ –Ω–∞–π–¥–µ–Ω: {department_id}")
                return False

            now = datetime.utcnow()
            new_user = db.User(
                user_department_id=department_id,
                user_organization_id=org_id,
                name=name,
                number=number,
                telegram_id=telegram_id,
            )
            history = db.History(
                data_start=now.date().isoformat(),
                data_7=(now + timedelta(days=7)).date().isoformat(),
                data_30=(now + timedelta(days=30)).date().isoformat(),
                data_90=(now + timedelta(days=90)).date().isoformat(),
            )
            new_user.chats.append(history)
            session.add(new_user)
            await session.commit()

            print(f"‚úÖ –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å {telegram_id} —É—Å–ø–µ—à–Ω–æ –∑–∞—Ä–µ–≥–∏—Å—Ç—Ä–∏—Ä–æ–≤–∞–Ω")
            
            # üî• –ê–≤—Ç–æ–∑–∞–ø—É—Å–∫ –æ–ø—Ä–æ—Å–æ–≤
            await schedule_polls_for_user(user_id=telegram_id, bot=bot)
            return True
        except Exception as e:
            print(f"[reg_users ERROR] {e}")
            import traceback
            traceback.print_exc()
            return False

# ====================== –ü–ª–∞–Ω–∏—Ä–æ–≤—â–∏–∫ –æ–ø—Ä–æ—Å–æ–≤ ======================
running_tasks = {}

async def schedule_polls_for_user(user_id: int, bot: Bot, force_restart: bool = False):
    """
    –ê–≤—Ç–æ–∑–∞–ø—É—Å–∫ –æ–ø—Ä–æ—Å–æ–≤ –¥–ª—è –æ–¥–Ω–æ–≥–æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è.
    """
    global running_tasks

    if user_id in running_tasks and not force_restart:
        print(f"‚ÑπÔ∏è –ó–∞–¥–∞—á–∏ —É–∂–µ –∑–∞–ø—É—â–µ–Ω—ã –¥–ª—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è {user_id}")
        return

    async def run_poll(poll_type: str, delay: int):
        await asyncio.sleep(delay)
        print(f"üöÄ –û—Ç–ø—Ä–∞–≤–∫–∞ –æ–ø—Ä–æ—Å–∞ {poll_type} –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é {user_id}")
        try:
            await start_poll(user_id=user_id, bot=bot, poll_type=poll_type)
        except Exception as e:
            print(f"[run_poll ERROR] {poll_type} –¥–ª—è {user_id}: {e}")
            import traceback
            traceback.print_exc()

    # –°–æ–∑–¥–∞—ë–º –∑–∞–¥–∞—á–∏ —Å —Ä–∞–∑–Ω—ã–º–∏ –∑–∞–¥–µ—Ä–∂–∫–∞–º–∏
    tasks = [
        asyncio.create_task(run_poll("week1", 604800)),    # 10 —Å–µ–∫—É–Ω–¥ –¥–ª—è —Ç–µ—Å—Ç–∏—Ä–æ–≤–∞–Ω–∏—è
        asyncio.create_task(run_poll("month1", 2678400)), # 1 –¥–µ–Ω—å (24 —á–∞—Å–∞)
        asyncio.create_task(run_poll("month3", 7776000)) # 30 –¥–Ω–µ–π
    ]
    
    running_tasks[user_id] = tasks
    print(f"‚úÖ –ó–∞–¥–∞—á–∏ –ø–ª–∞–Ω–∏—Ä–æ–≤—â–∏–∫–∞ —Å–æ–∑–¥–∞–Ω—ã –¥–ª—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è {user_id}")

    # –û—á–∏—Å—Ç–∫–∞ –∑–∞–¥–∞—á –ø–æ—Å–ª–µ –∑–∞–≤–µ—Ä—à–µ–Ω–∏—è
    for task in tasks:
        task.add_done_callback(lambda t, uid=user_id: running_tasks.pop(uid, None) if uid in running_tasks else None)

